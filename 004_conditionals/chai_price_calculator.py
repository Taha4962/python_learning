cup_size = input("Choose your cup size (small/medium/large): ").lower()

if cup_size == "small" :
    print("The price is ₹10 rupees")
elif cup_size == "medium":
    print("The price is ₹15 rupees")
elif cup_size == "large" : 
    print("The price is ₹20 rupees ")
else  :
    print("Unknown cup size")